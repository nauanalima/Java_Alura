
public class TestaCondicional {
	
	public static void main(String[] args) {
		
		System.out.println("Testando condicionais\n");
		int idade = 17;
		
		if (idade >= 18) {
			
			System.out.println("Você tem mais de 18 anos.");
			
		}
		
		else {
			
			System.out.println("Você é menor de idade.");
			
		}
	}

}
